package Models;

/**
 *
 * @author X1
 */
public class User {
    String userID;
    String passCode;

    public User() {
    }

    public User(String userID, String passCode) {
        this.userID = userID;
        this.passCode = passCode;
    }

    public String getUserID() {
        return userID;
    }

    public void setUserID(String userID) {
        this.userID = userID;
    }

    public String getPassCode() {
        return passCode;
    }

    public void setPassCode(String passCode) {
        this.passCode = passCode;
    }
}
